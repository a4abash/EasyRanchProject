// Deploy this as a web app to handle form submissions

function doPost(e) {
  try {
    // Check if event exists
    if (!e) {
      throw new Error('Invalid request: Missing event data');
    }
    
    let data;
    
    // Handle both JSON and FormData submissions
    if (e.postData && e.postData.contents) {
      // Try to parse as JSON first
      try {
        data = JSON.parse(e.postData.contents);
      } catch (jsonError) {
        // If JSON parsing fails, treat as form data
        data = e.parameter;
      }
    } else if (e.parameter) {
      // Handle form data
      data = e.parameter;
    } else {
      throw new Error('Invalid request: No POST data found');
    }
    
    // Log received data for debugging
    console.log('Received data:', JSON.stringify(data));
    
    // Get the active spreadsheet
    const spreadsheet = SpreadsheetApp.openById('1LHJWCf3Hu0dAieq4vvYsnGk5_ni4k-sloicubPtyKSE');
    const sheet = spreadsheet.getSheetByName('Sheet1'); // or your specific sheet name
    
    // Prepare the row data
    const rowData = [
      data.timestamp,    // TimeStamp
      data.source,       // Source (EasiAgri or EasyRanch)
      data.name,         // Name
      data.email,        // Email
      data.message       // Message
    ];
    
    console.log('Row data to append:', rowData);
    
    // Append the data to the sheet
    sheet.appendRow(rowData);
    
    console.log('Data successfully appended to sheet');
    
    // Send email notification
    sendEmailNotification(data);
    
    // Return success response
    return ContentService
      .createTextOutput(JSON.stringify({ 'result': 'success' }))
      .setMimeType(ContentService.MimeType.JSON);
      
  } catch (error) {
    // Log the error
    console.error('Error processing form submission:', error);
    
    // Return error response
    return ContentService
      .createTextOutput(JSON.stringify({ 'result': 'error', 'error': error.toString() }))
      .setMimeType(ContentService.MimeType.JSON);
  }
}

 

function sendEmailNotification(data) {
  try {
    const subject = `New Contact Form Submission - ${data.source}`;
    const body = `
New contact form submission received:

Source: ${data.source}
Name: ${data.name}
Email: ${data.email}
Message: ${data.message}
Timestamp: ${data.timestamp}

This message was automatically generated by the EasiAgri/EasyRanch contact form system.
    `;
    
    // Send email to enquiry@innova8s.com
    MailApp.sendEmail('enquiry@innova8s.com', subject, body);
    
    // Send auto-reply to the applicant
    sendAutoReply(data);
    
  } catch (error) {
    console.error('Error sending email notification:', error);
  }
}

